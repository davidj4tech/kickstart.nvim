local M = {}

function M.ensure_dirs(paths)
  vim.fn.mkdir(vim.fn.expand(paths.org_roam_dir), 'p')
  vim.fn.mkdir(vim.fn.expand(paths.org_roam_journal_dir), 'p')
  vim.fn.mkdir(vim.fn.expand(paths.org_roam_notes_dir), 'p')
  vim.fn.mkdir(vim.fn.expand(paths.org_roam_people_dir), 'p')
  vim.fn.mkdir(vim.fn.expand(paths.org_roam_projects_dir), 'p')
  vim.fn.mkdir(vim.fn.expand(paths.org_roam_refs_dir), 'p')
end

