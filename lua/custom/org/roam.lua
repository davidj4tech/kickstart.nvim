function M.dailies()
  return {
    directory = 'journal',
    templates = {
      d = {
        description = 'Daily journal',
        target = '%<%Y-%m-%d>.org',
        template = {
          '#+title: %<%A, %d %B %Y>',
          '#+filetags: Daily',
          '',
          '',
          '* %<%H:%M> %?',
        },
      },
    },
  }
end

return M
